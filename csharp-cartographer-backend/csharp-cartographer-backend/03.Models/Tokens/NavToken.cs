        public bool IsGenericTypeArgument()
        {
            //        ⌄
            // List<string> LeadingTrivia { get; set; }
            if (HasAncestorAt(1, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == "<"
                && NextToken?.Text == ">")
            {
                return true;
            }

            //         ⌄
            // List<string?> LeadingTrivia { get; set; }
            if (HasAncestorAt(2, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == "<"
                && NextToken?.Text == "?")
            {
                return true;
            }

            //       ⌄
            // Func<int, int> square = x => x * x;
            if (HasAncestorAt(1, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == "<"
                && NextToken?.Text == ",")
            {
                return true;
            }

            //            ⌄
            // Func<int, int> square = x => x * x;
            if (HasAncestorAt(1, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == ","
                && NextToken?.Text == ">")
            {
                return true;
            }

            //       ⌄
            // Func<int?, int> square = x => x * x;
            if (HasAncestorAt(2, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == "<"
                && NextToken?.Text == ",")
            {
                return true;
            }

            //            ⌄
            // Func<int, int?> square = x => x * x;
            if (HasAncestorAt(2, SyntaxKind.TypeArgumentList)
                && PrevToken?.Text == ","
                && NextToken?.Text == "?")
            {
                return true;
            }

            return false;
        }

